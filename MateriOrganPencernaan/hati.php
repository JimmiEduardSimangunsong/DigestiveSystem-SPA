    <h1>Pankreas, Hati dan Empedu</h1>
    <p>Hati adalah organ pelengkap dalam sistem pencernaan karena akan membentuk cairan empedu yang diperlukan dalam 
    proses pencernaan lemak. Empedu tersebut lalu akan ditampung di kantung empedu, sebelum digunakan di usus halus.
    Jadi, kantung empedu fungsinya menyalurkan empedu ke usus halus ya. Kalau hati yang akan menghasilkan empedu.
    Nah, kalau pankreas berfungsi untuk memproduksi enzim pencernaan untuk mencerna karbohidrat, protein dan lemak 
    di usus halus. Selain itu, pankreas juga akan menghasilkan senyawa bikarbonat, yang akan menetralkan makanan dari 
    lambung yang sifatnya asam, sehingga tidak melukai dinding usus halus.</p>
