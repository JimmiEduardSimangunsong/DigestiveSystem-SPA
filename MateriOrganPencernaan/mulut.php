    <h1>Mulut</h1>
    <p>Mulut berfungsi sebagai tempat masuknya makanan dan dimulainya proses pencernaan. 
    Ini karena di dalam mulut, terjadi pencernaan secara mekanik oleh gigi dan kimiawi oleh enzim amilase.
    Gigi akan menghancurkan makanan menjadi ukuran yang lebih kecil, sehingga memudahkan enzim 
    amilase bekerja. Enzim amilase lalu akan menguraikan kandungan pati atau amilum dalam makanan,
     menjadi gula sederhana yang dapat diserap tubuh. Nah, enzim amilase diproduksi oleh kelenjar ludah ya. 
     Selain itu, di mulut ada juga lidah, yang akan mengaduk makanan sehingga bisa bercampur dengan enzim amilase.</p>
