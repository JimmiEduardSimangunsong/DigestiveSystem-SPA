    <h1>Mulut</h1>
    <p>Mulut merupakan organ pertama dalam sistem pencernaan yang akan dilalui oleh makanan,
    didalam mulut terdapat gigi,ludah, dan air liur yang membantu dalam proses pencernaan.
    mulut memiliki fungsi utama yaitu sebagai berikut:<br>
    1.sebagai alat berbicara.<br>
    2. sebagai tempat keluarnya nafas selain hidung<br>
    3.sebagai tempat mengunyah dan menggiling makanan.<br>
    4.merasakan makanan.<br>
    5.menelan makanan.<br>
    dimulut juga terjadi pencernaan secara mekanis dan kiwiawi<br>
    <h3>1.Pencernaan Mekanik:</h3>
    <p>terjadi pemecahan makanan yang berukuran beasar (makromolekul)
    menjadi ukuran yang lebih kecil (mikromolekul). proses ini
    dibantu oleh airliur dan gigi, untuk pemotongan makanan dibantu oleh
    gigi seri dan taring kemudian, makanan dikunyah dengan
    menggunakan gigi geraham depan (premolar) dan graham belakang (molar)
    </p><br>
    <h3>2.Pencernaan Kimiawi :</h3>
    <p> proses pencernaan dikenal sebagai pencernaan secara hidrolisis.<br>
    Hidrolisis berarti membelah molekul besar menjadi molekul yg kecil yang
    mampu dilarutkan dalam air. proses ini dibantu oleh bantuan enzim amilase 
    atau ptialin yang berfungsi memecah karbohdrat yang kompleks menjadi
    rantai yang lebih kecil atau menjadi gula yang lebih sederhana.</p>
