    <h1>Usus Besar</h1>
    <p>Usus besar adalah organ pencernaan yang terhubung dengan usus halus. Sisa makanan yang tidak bisa dicerna dan 
    tadi membentuk feses, dengan dibantu oleh bakteri Escherichia coli. Nah, di usus besar terdapat beberapa bagian, 
    yaitu:
    1.kolon sebagai tempat pemadatan feses atau penyerapan kembali air dari zat sisa makanan<br>
    2.rektum sebagai tempat menyimpan feses sementara waktu.<br>
    Selain itu, ada pula umbai cacing, yaitu bagian yang berbentuk memanjang seperti cacing. Bagian ini bisa 
    membengkak jika ada sisa makanan yang tersumbat di dalamnya, lalu menimbulkan penyakit usus buntu.<br></p>
    <h1>Anus</h1>
    <p>Berfungsi sebagai tempat pembuangan zat sisa makanan atau feses. </p>
