    <h1>Faring dan Esofagus</h1>
    <p>Tenggorokan (faring) merupakan saluran pencernaan yang menghubungkan rongga mulut ke kerongkongan
         (esofagus). Makanan yang ditelan dari mulut masuk melalui Faring dan diteruskan ke kerongkongan. 
         Pada dinding kerongkongan 
        terjadi gerakan peristaltik, yaitu gerakan meremas-remas yang mendorong makanan menuju lambung.</p>
