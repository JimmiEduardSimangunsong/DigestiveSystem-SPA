    <h1>Lambung</h1>
    <p>Di lambung terjadi pencernaan kimiawi, dimana makanan dicerna oleh enzim dalam getah lambung yang 
    dihasilkan oleh sel kelenjar dinding lambung. Getah lambung terdiri dari:<br>
    1.Pepsin: enzim yang fungsinya memecah protein menjadi pepton<br>
    2.Asam Klorida (HCl): asam yang berfungsi untuk membunuh kuman dan bakteri pada makananan dan mengaktifkan 
    pepsinogen menjadi pepsin.<br>
    fungsi lambung untuk:<br>
    1.Mencerna protein<br>
    2.Menyimpan makanan (selama 2-5 jam)<br>
    3.Mematikan mikroorgansime berbahaya yang ada di lambung karena ada asam lambung<br>
    Setelah melalui pencernaan di lambung, makanan akan perlahan-perlahan didorong masuk ke usus halus.
</p>
