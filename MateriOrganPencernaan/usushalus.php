    <h1>Usus Halus</h1>
    <p>Usus halus merupakan organ pencernaan yang terletak di antara lambung dan usus besar. Bentuknya berupa saluran dengan panjang 
    sekitar 670 cm sampai 760 cm. Nah, usus halus bisa dibedakan lagi menjadi 3 bagian, yaitu duodenum, jejunum, dan ileum.
    Duodenum (usus dua belas jari) berfungsi sebagai tempat pencernaan makanan secara kimiawi. Caranya, makanan dari 
    lambung yang masuk ke duodenum akan dinetralkan dulu oleh senyawa bikarbonat dari pankreas. Lalu, lanjut dicerna 
    menggunakan enzim amilase, lipase, dan tripsin dari pankreas, serta enzim maltase yang dihasilkan usus halus itu sendiri.<br>
    Amilase akan memecah amilum jadi maltosa. Maltosanya lalu lanjut dipecah jadi glukosa oleh enzim maltase. Kalau lipase memecah 
    lemak jadi asam lemak dan gliserol. Ini dilakukan dengan bantuan empedu ya, yang akan mengemulsikan lemak sehingga enzim lipase 
    bisa bekerja. Sementara itu, tripsin akan memecah pepton jadi asam amino. Nah, glukosa, asam lemak, gliserol, dan asam amino 
    tadi merupakan bentuk zat gizi sederhana yang siap diserap tubuh. Penyerapannya terjadi di bagian usus halus berikutnya yaitu 
    jejunum dan ileum. Berarti jejunum (usus kosong) dan ileum (usus penyerapan) sama-sama berfungsi sebagai tempat penyerapan sari 
    makanan atau zat gizi sederhana. Makanya, strukturnya dipenuhi vili atau jonjot usus yang berfungsi memperluas area penyerapan sari 
    makanan.</p>
