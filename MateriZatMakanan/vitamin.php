
    <h1>VITAMIN</h1>
        <p>Vitamin selain berfungi sebagai pengatur metabolisme tubuh, juga berfungsi<br>
        untuk melindungi tubuh dari berbagai macam penyakit.vitamin dikelompokan<br>
        manjadi dua yaitu vitamin yang larut dalam air (Vitamin B dan C) dan vitamin<br>
        yang larut dalam lemak (vitamin A,D,E,K).<br>
        Berikut contohnya :<br>
        Vitamin A, berfungsi meningkatkan daya tahan tubuh dan menjaga kesehatan mata.<br>
        Vitamin B, berfungsi mencegah penyakit beri-beri dan meningkatkan nafsu makan.<br>
        Vitamin C, berfungsi mencegah sariawan, gusi berdarah, dan bibir pecah-pecah.<br>
        Vitamin D, berfungsi mencegah penyakit rakhitis dan osteoporosis, membentuk tulang dan gigi.<br>
        Vitamin E, berfungsi menyuburkan rambut, menghaluskan kulit, dan mencegah kemandulan.<br>
        Vitamin K, berfungsi membantu proses pembekuan darah.<br>
    </p>
    

