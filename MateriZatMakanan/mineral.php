<h1>MINERAL</h1>
    <p>Mineral merupakan nutrisi yang sedikit mengandung atom karbon. mineral<br>
    berfungsi sebagai zat pengatur metabolisme tubuh,pembangun sel,membantu<br>
    reaksi kimia tubuh,mengangkut oksigen keseluruh tubuh dan sebagai <br>
    pemelihara tulang.berikut sumber makanan yang mengandung banyak mineral<br>
    yaitu:<br>
    1. Gandum utuh ( oat, roti gandum, dan sereal gandum.)<br>
    2. Daging (daging sapi dan daging ayam)<br>
    3. Sayur (Sayuran berdaun gelap seperti bayam, brokoli, kale, dan sawi)<br>
    4. Kacang-kacangan(kacang tanah dan kacang almond )<br>
    5. Telur<br>
    6. Hasil olahan laut (Hasil olahan laut terutama tiram)<br>
    7. Buah (Alpukat dan pisang)<br>
    8. Susu (susu kambing dan susu sapi)
    </p>
