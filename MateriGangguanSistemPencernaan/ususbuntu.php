    <p>
    Kamu pasti familiar deh dengan kata-kata “radang usus buntu”. Hayo, biasanya, radang usus buntu 
    selalu dikaitkan dengan gosip “Gara-gara makan cabai nih ya?” Seolah-olah dengan banyak makan cabai, 
    akan ada biji cabai yang tersasar hingga ke usus buntu dan membuat peradangan di sana.
    Tapi, benarkah?<br>
    Ternyata, itu hanyalah mitos.secara medis, peradangan usus buntu memang dapat disebabkan oleh berbagai faktor.
     Tapi, bukan berarti ada makanan tertentu yang membuat munculnya penyakit radang usus buntu. Berdasarkan penuturan 
     Dr Ari Fahrial kepada Detik Health, penyebab usus buntu bisa beragam. Seperti makanan yang kotor dan tidak higienis,
      serta penyumbatan usus buntu (masuknya benda asing, sel kanker, dan feses) juga dapat menyebabkan peradangan.<br>
    Salah satu tanda paling nyata dari seseorang yang mengalami peradangan usus buntu adalah timbul perasaan nyeri di bagian 
    bawah perut sebelah kanan. Kalau nyerinya berada di bagian hati, mungkin itu yang dinamakan cemburu (???).
    Jika seseorang mengalami ini, pengobatan paling standar adalah dengan melakukan operasi pengangkatan usus buntu.
