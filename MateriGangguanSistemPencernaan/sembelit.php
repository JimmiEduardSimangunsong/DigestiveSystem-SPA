    <h1>Sembelit (Konstipasi)</h1>
    <p>Berkebalikan dengan diare, sembelit atau konstipasi adalah gangguan pada sistem pencernaan yang disebabkan oleh 
        penyerapan air yang berlebihan. Ada berbagai alasan yang menyebabkan terjadinya sembelit. Seperti misalnya, 
        kurang minum, kurang makan berserat, tidak membiasakan diri buang air besar setiap hari, dan stres. Kalau kamu 
        kurang minum setiap hari, hal itu akan membuat asupan feses di dalam tubuh mengalami kekurangan air sehingga 
        lebih keras dan kita harus lebih semangat untuk ‘Hmmmmhh… Plung!’ di kamar mandi.<br>
        Bagaimana cara untuk mencegah sembelit?<br>
        Banyak minum air putih, olahraga rutin, makan makanan yang mengandung serat.

